package B5_Baekjoon;

import java.util.Scanner;

public class Baekjoon11718 {
    public static void main(String[] args) {
        var scan = new Scanner(System.in);

        while(scan.hasNextLine()){
            String str = scan.nextLine();
            System.out.println(str);
        }
        scan.close();

    }
}
